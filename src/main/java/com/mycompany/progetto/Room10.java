    private void sfondobuioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sfondobuioActionPerformed
        if (findObject(torcia) >= 0) {

            sfondobuio.setVisible(false);
            acceso++;
            text.setText("Hai appoggiato la torcia su un ferro incagliato in un muro");
            Timer timer = new Timer(3000, event -> {
                text.setText("");
            });
            timer.setRepeats(false);
            timer.start();
            removeObject(torcia);
        }
    }//GEN-LAST:event_sfondobuioActionPerformed

