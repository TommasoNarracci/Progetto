    private void nordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nordActionPerformed
        if (acceso >= 1 || north.isEnable() == 1) {

            if (acceso >= 1) {
                north.setEnable(1);

            }

            goNorth(this);

        } else {
            text.setText("Non si vede niente,non puoi proseguire... ");
            Timer timer = new Timer(3000, event -> {
                text.setText("");
            });
            timer.setRepeats(false);
            timer.start();
        }

    }//GEN-LAST:event_nordActionPerformed

